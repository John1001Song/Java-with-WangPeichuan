package nestedClasses;
/** A class representing color. Stores a string representation of the color (such as "red") 
*/
public class Color {
	private String col;

	public Color(String col) {
		this.col = col;
	}
	
	public String getColor() {
		return col;
	}
	
	public String toString() {
		return col;
	}
}
